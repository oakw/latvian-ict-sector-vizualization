// Helper function to get a translation with variables replaced
export function getTranslatedText(language, key, variables = {}) {
  if (!translations[language] || !translations[language][key]) {
    // Fallback to English or return the key itself
    const fallbackText = translations['en'][key] || key;
    return replaceVariables(fallbackText, variables);
  }

  return replaceVariables(translations[language][key], variables);
}

// Helper function to replace variables in translation strings
function replaceVariables(text, variables) {
  let result = text;
  for (const [key, value] of Object.entries(variables)) {
    result = result.replace(new RegExp(`{${key}}`, 'g'), value);
  }
  return result;
}
